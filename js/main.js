// Navbar

function navbar_responsive() {
  var x = document.getElementById("id_navbar");
  if (x.className === "navbar") {
    x.className += " responsive";
  } else {
    x.className = "navbar";
  }
}

/*Scroll to top when arrow up clicked*/
$(window).scroll(function() {
    var height = $(window).scrollTop();
    var scrollPercent = Math.round(100 * $(window).scrollTop() / ($(document).height() - $(window).height()));
    if (scrollPercent > 10) {
        if (scrollPercent < 90) {
          $('#back_to_top').fadeIn();
        } else {
          $('#back_to_top').fadeOut();
        }
    } else {
      $('#back_to_top').fadeOut();
    }
});
$(document).ready(function() {
    $("#back_to_top").click(function(event) {
        event.preventDefault();
        $("html, body").animate({scrollTop: 0}, 900);
        return false;
    });

});

function go_back() {
  window.history.back();
}


/*Scroll to bottom when arrow down clicked*/
$(window).scroll(function() {
    var height = $(window).scrollTop();
    var scrollPercent = Math.round(100 * $(window).scrollTop() / ($(document).height() - $(window).height()));
    if (scrollPercent > 10) {
        if (scrollPercent < 90) {
          $('#down_to_bottom').fadeIn();
        } else {
          $('#down_to_bottom').fadeOut();
        }
    } else {
        $('#down_to_bottom').fadeOut();
    }
});
$(document).ready(function() {
    $("#down_to_bottom").click(function(event) {
        event.preventDefault();
        $("html, body").animate({scrollTop:$(document).height()}, 900);
        return false;
    });

});


